export const canonBooks: CanonBook[] = books.map(([abbr, name, cat, theme, clue], order) => {
  const category = canonCategories.find((item) => item.id === cat);

  if (!category) {
    throw new Error(`Unknown canon category: ${cat}`);
  }

  return {
    order,
    abbr,
    name,
    cat,
    theme,
    clue,
    testament: category.testament,
  };
});
